from typing import Dict, List

_A = "{{ context.asset_id }}"

def _cmd(nid: str, name: str, command: str, confirm: bool = False, retry: int = 0) -> dict:
    return {"id": nid, "name": name, "action_type": "run_command",
            "payload_template": {"command": command, "asset_id": _A},
            "requires_confirm": confirm, "retry_count": retry}

def _act(nid: str, name: str, action_type: str, confirm: bool = False, retry: int = 0, **payload) -> dict:
    payload.setdefault("asset_id", _A)
    return {"id": nid, "name": name, "action_type": action_type,
            "payload_template": payload, "requires_confirm": confirm, "retry_count": retry}

def _edges(*ids) -> list:
    return [{"source": ids[i], "target": ids[i + 1]} for i in range(len(ids) - 1)]

def _tpl(name: str, desc: str, cat: str, trig: str, cond: dict, risk: str, nodes: list, enabled: bool = True) -> dict:
    return {"name": name, "description": desc, "category": cat, "trigger_type": trig,
            "trigger_condition": cond, "risk_level": risk, "enabled": enabled,
            "nodes": nodes, "edges": _edges(*[n["id"] for n in nodes])}


